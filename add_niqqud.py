file_path = 'c:/git/hebrew_crossword/src/words.ts'
with open(file_path, 'r', encoding='utf-8') as f:
    content = f.read()

# Replace interface
if 'wordWithNiqqud?: string;' not in content:
    content = content.replace(
        '  word: string;',
        '  word: string;\n  wordWithNiqqud?: string;'
    )

def add_niqqud_to_line(match):
    line = match.group(0)
    word = match.group(1)
    if 'wordWithNiqqud:' in line:
        return line
    niqqud = niqqud_map.get(word, word)
    # Insert wordWithNiqqud after word: 'xxx'
    return line.replace(f"word: '{word}'", f"word: '{word}', wordWithNiqqud: '{niqqud}'")

new_content = re.sub(r"\{\s*word:\s*'([^']+)'[^}]+\}", add_niqqud_to_line, content)

with open(file_path, 'w', encoding='utf-8') as f:
    f.write(new_content)

print('Done applying Niqqud.')
